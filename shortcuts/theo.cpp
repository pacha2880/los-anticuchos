// Erdős–Gallai theorem
// A sequence of non-negative integers d1>=...>=d_n can be represented 
// as the degree sequence of a finite simple graph on n vertices if and only if 
// d1+...+dn is even and sum(i=1->k, di) <= k*(k - 1) + sum(i=k+1->n, min(di, k))
// holds for every k in 1<=k<=n
// teorema de Erdős-Szekeres
// Para r, s dados, cualquier sucesión de longitud al menos (r-1)(s-1)+1 contiene 
// una subsucesión monótonamente creciente de longitud r o una subsucesión 
// monótonamente decreciente de longitud s
// The harmonic lemma
// (n/1, n/2, ..., n/n) has at most 2*sqrt(n) different elements
// greatest x that satisfy d(x) = d(i) is n / (n / i)